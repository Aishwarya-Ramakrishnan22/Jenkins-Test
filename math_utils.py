"""
Simple Math Functions
=====================
Basic functions for Jenkins CI/CD learning.
"""


def add(a, b):
    """Return the sum of two numbers."""
    return a + b


def multiply(a, b):
    """Return the product of two numbers."""
    return a * b


def subtract(a, b):
    """Return the difference of two numbers."""
    return a - b


def is_even(n):
    """Check if a number is even."""
    return n % 2 == 0
